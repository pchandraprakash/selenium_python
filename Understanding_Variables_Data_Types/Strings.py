"""
Strings are sequence of characters
String literals may be delimited using either single or double quotes.
"""
a = "this is a string with double quotes"
b = 'this is a string with single quotes'
print(a)
print(b)

c = 'there is a \'quote\' in this string'
d = "there is a 'quote' in this string"
e = 'there is a "quote" in this string'
print(c)
print(d)
print(e)

print("***********")
print('foo\\bar')
print('foo\tbar')
print('foo\nbar')
